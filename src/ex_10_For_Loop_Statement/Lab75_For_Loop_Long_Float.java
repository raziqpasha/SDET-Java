package ex_10_For_Loop_Statement;

public class Lab75_For_Loop_Long_Float {
    public static void main(String[] args) {
        for(long i =123456788910l; i>=10;i++){
            System.out.println(i); //
        }

        for(float f=1.0f;f<=10.0;f++){
            System.out.println(f); // 1.0-------------------10.0
        }
    }
}
